const subtract = (a: number, b: number): number => {
  return a - b;
};

function divide(a: number, b: number): number {
  return a / b;
}

const multiply = function (a: number, b: number): number {
  return a * b;
};

const logger = (message: string): void => {
  console.log(message);
};
// fn that does not return anything should return void & tech a fn that returns void can return null or undefined

const throwError = (message: string): never => {
  throw new Error(message);
};
// 'this fn will never completely finish executing', very rare to use never like this, only use when we NEVER return anything

const todaysWeather = {
  date: new Date(),
  weather: 'sunny',
};

const logWeather = ({
  date,
  weather,
}: {
  date: Date;
  weather: string;
}): void => {
  console.log(date);
  console.log(weather);
};

logWeather(todaysWeather);
